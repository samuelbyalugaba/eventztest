-- Consolidated Eventz Supabase schema.
-- Built from every SQL migration in supabase/migrations and supabase/migrations/archive,
-- with project/runtime objects included where the app expects them.
--
-- Intended for a fresh Supabase project. Review carefully before running against
-- a populated database, because it represents the final schema state rather than
-- the historical step-by-step data transformations.

