CREATE OR REPLACE FUNCTION public.become_organizer(
  p_full_name TEXT,
  p_username TEXT,
  p_organizer_type TEXT,
  p_location TEXT,
  p_bio TEXT,
  p_avatar_url TEXT,
  p_contact_email TEXT DEFAULT NULL
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_user_id UUID;
  v_result JSONB;
BEGIN
  v_user_id := auth.uid();

  IF v_user_id IS NULL THEN
    RAISE EXCEPTION 'Not authenticated';
  END IF;

  IF EXISTS (
    SELECT 1
    FROM public.profiles
    WHERE username = p_username
      AND id <> v_user_id
  ) THEN
    RAISE EXCEPTION 'Username already taken';
  END IF;

  PERFORM set_config('app.bypass_profile_trigger', 'true', true);

  UPDATE public.profiles
  SET
    full_name = p_full_name,
    username = p_username,
    organizer_type = p_organizer_type,
    location = p_location,
    bio = p_bio,
    avatar_url = p_avatar_url,
    contact_email = COALESCE(p_contact_email, contact_email),
    is_organizer = true,
    updated_at = now()
  WHERE id = v_user_id
  RETURNING to_jsonb(public.profiles.*) INTO v_result;

  PERFORM set_config('app.bypass_profile_trigger', 'false', true);

  RETURN v_result;
END;
$$;

CREATE OR REPLACE FUNCTION public.downgrade_to_personal_account()
RETURNS void
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  current_user_id UUID;
BEGIN
  current_user_id := auth.uid();

  IF current_user_id IS NULL THEN
    RAISE EXCEPTION 'Not authenticated';
  END IF;

  PERFORM set_config('app.bypass_profile_trigger', 'true', true);

  UPDATE public.profiles
  SET is_organizer = false,
      updated_at = now()
  WHERE id = current_user_id;

  PERFORM set_config('app.bypass_profile_trigger', 'false', true);
END;
$$;

CREATE OR REPLACE FUNCTION public.purchase_ticket(
  p_event_id BIGINT,
  p_ticket_type TEXT,
  p_customer_name TEXT,
  p_customer_email TEXT,
  p_ticket_number TEXT,
  p_qr_code TEXT,
  p_user_id UUID DEFAULT NULL,
  p_price TEXT DEFAULT NULL,
  p_transaction_id BIGINT DEFAULT NULL
)
RETURNS json
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_ticket_tiers JSONB;
  v_new_ticket_id BIGINT;
  v_updated_tiers JSONB;
  v_available_count INT;
  v_tier_found BOOLEAN := false;
  v_event_base_price TEXT;
  v_final_user_id UUID;
  v_event_price TEXT;
  v_required_amount NUMERIC;
  v_tx_status TEXT;
  v_tx_amount NUMERIC;
  v_tx_user UUID;
  v_tx_ticket_id BIGINT;
  v_tier JSONB;
BEGIN
  v_final_user_id := COALESCE(p_user_id, auth.uid());

  IF v_final_user_id IS NULL THEN
    RAISE EXCEPTION 'User must be logged in';
  END IF;

  IF p_transaction_id IS NULL THEN
    RAISE EXCEPTION 'Transaction id required';
  END IF;

  SELECT ticket_tiers, price
  INTO v_ticket_tiers, v_event_base_price
  FROM public.events
  WHERE id = p_event_id
  FOR UPDATE;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Event not found';
  END IF;

  IF v_ticket_tiers IS NOT NULL AND jsonb_array_length(v_ticket_tiers) > 0 THEN
    FOR v_tier IN SELECT * FROM jsonb_array_elements(v_ticket_tiers)
    LOOP
      IF v_tier->>'name' = p_ticket_type THEN
        v_event_price := v_tier->>'price';
        v_tier_found := true;

        IF v_tier ? 'available' THEN
          v_available_count := (v_tier->>'available')::int;
          IF v_available_count <= 0 THEN
            RAISE EXCEPTION 'Tickets sold out for this tier';
          END IF;
        END IF;

        EXIT;
      END IF;
    END LOOP;

    IF NOT v_tier_found THEN
      RAISE EXCEPTION 'Invalid ticket type';
    END IF;

    SELECT jsonb_agg(
      CASE
        WHEN elem->>'name' = p_ticket_type AND (elem ? 'available') THEN
          jsonb_set(elem, '{available}', ((elem->>'available')::int - 1)::text::jsonb)
        ELSE elem
      END
    )
    INTO v_updated_tiers
    FROM jsonb_array_elements(v_ticket_tiers) AS elem;

    UPDATE public.events
    SET ticket_tiers = v_updated_tiers
    WHERE id = p_event_id;
  ELSE
    v_event_price := COALESCE(v_event_base_price, p_price, '0');
  END IF;

  v_required_amount := NULLIF(regexp_replace(COALESCE(v_event_price, '0'), '[^0-9\.]', '', 'g'), '')::numeric;
  IF v_required_amount IS NULL THEN
    v_required_amount := 0;
  END IF;

  SELECT status, amount, user_id, ticket_id
  INTO v_tx_status, v_tx_amount, v_tx_user, v_tx_ticket_id
  FROM public.transactions
  WHERE id = p_transaction_id
  FOR UPDATE;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Transaction not found';
  END IF;

  IF v_tx_user IS DISTINCT FROM v_final_user_id THEN
    RAISE EXCEPTION 'Transaction does not belong to user';
  END IF;

  IF v_tx_ticket_id IS NOT NULL THEN
    RAISE EXCEPTION 'Transaction already used';
  END IF;

  IF lower(coalesce(v_tx_status, '')) NOT IN ('completed', 'success') THEN
    RAISE EXCEPTION 'Transaction not completed';
  END IF;

  IF v_tx_amount < v_required_amount THEN
    RAISE EXCEPTION 'Insufficient transaction amount';
  END IF;

  INSERT INTO public.tickets (
    user_id,
    event_id,
    ticket_number,
    barcode,
    price,
    purchase_date,
    customer_name,
    customer_email,
    ticket_type,
    status,
    qr_code
  )
  VALUES (
    v_final_user_id,
    p_event_id,
    p_ticket_number,
    p_ticket_number,
    v_event_price,
    now(),
    p_customer_name,
    p_customer_email,
    p_ticket_type,
    'valid',
    p_qr_code
  )
  RETURNING id INTO v_new_ticket_id;

  UPDATE public.transactions
  SET ticket_id = v_new_ticket_id,
      updated_at = now()
  WHERE id = p_transaction_id;

  RETURN json_build_object('id', v_new_ticket_id, 'status', 'success');
END;
$$;

CREATE OR REPLACE FUNCTION public.purchase_ticket(
  p_event_id BIGINT,
  p_ticket_type TEXT,
  p_customer_name TEXT,
  p_customer_email TEXT,
  p_ticket_number TEXT,
  p_qr_code TEXT,
  p_user_id UUID DEFAULT NULL,
  p_price TEXT DEFAULT NULL
)
RETURNS json
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_event_price TEXT;
  v_ticket_tiers JSONB;
  v_new_ticket_id BIGINT;
  v_updated_tiers JSONB;
  v_available_count INT;
  v_tier_found BOOLEAN := false;
  v_event_base_price TEXT;
  v_final_user_id UUID;
  v_tier JSONB;
BEGIN
  v_final_user_id := COALESCE(p_user_id, auth.uid());

  IF v_final_user_id IS NULL THEN
    RAISE EXCEPTION 'User must be logged in or user_id provided';
  END IF;

  SELECT ticket_tiers, price
  INTO v_ticket_tiers, v_event_base_price
  FROM public.events
  WHERE id = p_event_id
  FOR UPDATE;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Event not found';
  END IF;

  IF v_ticket_tiers IS NOT NULL AND jsonb_array_length(v_ticket_tiers) > 0 THEN
    FOR v_tier IN SELECT * FROM jsonb_array_elements(v_ticket_tiers)
    LOOP
      IF v_tier->>'name' = p_ticket_type THEN
        v_event_price := v_tier->>'price';
        v_tier_found := true;

        IF v_tier ? 'available' THEN
          v_available_count := (v_tier->>'available')::int;
          IF v_available_count <= 0 THEN
            RAISE EXCEPTION 'Tickets sold out for this tier';
          END IF;
        END IF;

        EXIT;
      END IF;
    END LOOP;

    IF NOT v_tier_found THEN
      RAISE EXCEPTION 'Invalid ticket type or ticket type not found for this event';
    END IF;

    SELECT jsonb_agg(
      CASE
        WHEN elem->>'name' = p_ticket_type AND (elem ? 'available') THEN
          jsonb_set(elem, '{available}', ((elem->>'available')::int - 1)::text::jsonb)
        ELSE elem
      END
    )
    INTO v_updated_tiers
    FROM jsonb_array_elements(v_ticket_tiers) AS elem;

    UPDATE public.events
    SET ticket_tiers = v_updated_tiers
    WHERE id = p_event_id;
  ELSE
    v_event_price := COALESCE(v_event_base_price, p_price, '0');
  END IF;

  INSERT INTO public.tickets (
    user_id,
    event_id,
    ticket_number,
    barcode,
    price,
    purchase_date,
    customer_name,
    customer_email,
    ticket_type,
    status,
    qr_code
  )
  VALUES (
    v_final_user_id,
    p_event_id,
    p_ticket_number,
    p_ticket_number,
    v_event_price,
    now(),
    p_customer_name,
    p_customer_email,
    p_ticket_type,
    'valid',
    p_qr_code
  )
  RETURNING id INTO v_new_ticket_id;

  RETURN json_build_object('id', v_new_ticket_id, 'status', 'success');
END;
$$;

