  SELECT count(*) INTO v_total_events
  FROM public.events
  WHERE organizer_id = target_user_id;

  SELECT count(*) INTO v_followers
  FROM public.follows
  WHERE following_id = target_user_id;

  SELECT COALESCE(sum(views), 0) INTO v_event_views
  FROM public.events
  WHERE organizer_id = target_user_id;

  SELECT COALESCE(sum(views), 0) INTO v_post_views
  FROM public.posts
  WHERE user_id = target_user_id;

  SELECT COALESCE(sum(views), 0) INTO v_media_views
  FROM public.user_media
  WHERE user_id = target_user_id;

  v_total_views := v_event_views + v_post_views + v_media_views;

  SELECT count(*) INTO v_live_streams
  FROM public.events
  WHERE organizer_id = target_user_id
    AND (streaming->>'available')::boolean IS true;

  SELECT
    count(*),
    COALESCE(sum(
      CASE
        WHEN t.price IS NULL OR t.price = 'Free' THEN 0
        ELSE COALESCE(NULLIF(regexp_replace(t.price, '[^0-9]', '', 'g'), '')::integer, 0)
      END
    ), 0)
  INTO v_tickets_sold, v_revenue
  FROM public.tickets t
  JOIN public.events e ON t.event_id = e.id
  WHERE e.organizer_id = target_user_id;

  RETURN json_build_object(
    'totalEvents', v_total_events,
    'followers', v_followers,
    'totalViews', v_total_views,
    'ticketsSold', v_tickets_sold,
    'revenue', v_revenue,
    'liveStreams', v_live_streams,
    'avgRating', 0
  );
END;
$$;

CREATE OR REPLACE FUNCTION public.get_event_analytics(target_event_id BIGINT)
RETURNS json
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_views BIGINT;
  v_interested_count BIGINT;
  v_tickets_sold BIGINT;
  v_shares_count BIGINT;
  v_revenue BIGINT;
  v_interested_last_7 BIGINT;
  v_interested_prev_7 BIGINT;
  v_tickets_last_7 BIGINT;
  v_tickets_prev_7 BIGINT;
  v_shares_last_7 BIGINT;
  v_shares_prev_7 BIGINT;
  v_daily_activity INTEGER[];
  v_location_counts json;
  v_age_counts json;
BEGIN
  SELECT views INTO v_views
  FROM public.events
  WHERE id = target_event_id;

  SELECT count(*) INTO v_interested_count
  FROM public.saved_events
  WHERE event_id = target_event_id;

  SELECT count(*) INTO v_shares_count
  FROM public.posts
  WHERE event_id = target_event_id;

  SELECT
    count(*),
    COALESCE(sum(
      CASE
        WHEN price IS NULL OR price = 'Free' THEN 0
        ELSE COALESCE(NULLIF(regexp_replace(price, '[^0-9]', '', 'g'), '')::integer, 0)
      END
    ), 0)
  INTO v_tickets_sold, v_revenue
  FROM public.tickets
  WHERE event_id = target_event_id;

  SELECT
    count(*) FILTER (WHERE created_at >= now() - interval '7 days'),
    count(*) FILTER (WHERE created_at >= now() - interval '14 days' AND created_at < now() - interval '7 days')
  INTO v_interested_last_7, v_interested_prev_7
  FROM public.saved_events
  WHERE event_id = target_event_id;

  SELECT
    count(*) FILTER (WHERE created_at >= now() - interval '7 days'),
    count(*) FILTER (WHERE created_at >= now() - interval '14 days' AND created_at < now() - interval '7 days')
  INTO v_tickets_last_7, v_tickets_prev_7
  FROM public.tickets
  WHERE event_id = target_event_id;

  SELECT
    count(*) FILTER (WHERE created_at >= now() - interval '7 days'),
    count(*) FILTER (WHERE created_at >= now() - interval '14 days' AND created_at < now() - interval '7 days')
  INTO v_shares_last_7, v_shares_prev_7
  FROM public.posts
  WHERE event_id = target_event_id;

  WITH activity AS (
    SELECT created_at FROM public.saved_events WHERE event_id = target_event_id AND created_at >= now() - interval '7 days'
    UNION ALL
    SELECT created_at FROM public.tickets WHERE event_id = target_event_id AND created_at >= now() - interval '7 days'
    UNION ALL
    SELECT created_at FROM public.posts WHERE event_id = target_event_id AND created_at >= now() - interval '7 days'
  )
  SELECT array_agg(d)
  INTO v_daily_activity
  FROM (
    SELECT count(a.created_at)::integer AS d
    FROM generate_series(0, 6) AS day_idx
    LEFT JOIN activity a
      ON (EXTRACT(ISODOW FROM a.created_at)::int - 1) = day_idx
    GROUP BY day_idx
    ORDER BY day_idx
  ) t;

  IF v_daily_activity IS NULL THEN
    v_daily_activity := ARRAY[0,0,0,0,0,0,0];
  END IF;

  WITH unique_users AS (
    SELECT user_id FROM public.tickets WHERE event_id = target_event_id
    UNION
    SELECT user_id FROM public.saved_events WHERE event_id = target_event_id
  ),
  user_locs AS (
    SELECT split_part(p.location, ',', 1) AS city
    FROM unique_users u
    JOIN public.profiles p ON u.user_id = p.id
    WHERE p.location IS NOT NULL AND p.location <> ''
  )
  SELECT json_object_agg(city, count)
  INTO v_location_counts
  FROM (
    SELECT city, count(*) AS count
    FROM user_locs
    GROUP BY city
    ORDER BY count DESC
    LIMIT 5
  ) t;

  WITH unique_users AS (
    SELECT user_id FROM public.tickets WHERE event_id = target_event_id
    UNION
    SELECT user_id FROM public.saved_events WHERE event_id = target_event_id
  ),
  user_ages AS (
    SELECT EXTRACT(YEAR FROM AGE(current_date, p.birthdate)) AS age
    FROM unique_users u
    JOIN public.profiles p ON u.user_id = p.id
    WHERE p.birthdate IS NOT NULL
  )
  SELECT json_build_object(
    '18-24', count(*) FILTER (WHERE age BETWEEN 18 AND 24),
    '25-34', count(*) FILTER (WHERE age BETWEEN 25 AND 34),
    '35-44', count(*) FILTER (WHERE age BETWEEN 35 AND 44),
    '45+', count(*) FILTER (WHERE age >= 45)
  )
  INTO v_age_counts
  FROM user_ages;

  RETURN json_build_object(
    'views', COALESCE(v_views, 0),
    'interested', v_interested_count,
    'ticketsSold', v_tickets_sold,
    'shares', v_shares_count,
    'revenue', v_revenue,
    'trends', json_build_object(
      'interested', json_build_object('last7', v_interested_last_7, 'prev7', v_interested_prev_7),
      'tickets', json_build_object('last7', v_tickets_last_7, 'prev7', v_tickets_prev_7),
      'shares', json_build_object('last7', v_shares_last_7, 'prev7', v_shares_prev_7)
    ),
    'dailyActivity', v_daily_activity,
    'demographics', json_build_object(
      'locations', COALESCE(v_location_counts, '{}'::json),
      'ageGroups', COALESCE(v_age_counts, json_build_object('18-24', 0, '25-34', 0, '35-44', 0, '45+', 0))
    )
  );
END;
$$;

