CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  INSERT INTO public.profiles (id, email, full_name, avatar_url)
  VALUES (
    NEW.id,
    NEW.email,
    COALESCE(NEW.raw_user_meta_data->>'name', NEW.raw_user_meta_data->>'full_name'),
    NEW.raw_user_meta_data->>'avatar_url'
  )
  ON CONFLICT (id) DO UPDATE
  SET
    email = EXCLUDED.email,
    full_name = COALESCE(public.profiles.full_name, EXCLUDED.full_name),
    avatar_url = COALESCE(public.profiles.avatar_url, EXCLUDED.avatar_url);

  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION public.check_profile_updates()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
  v_bypass TEXT;
BEGIN
  BEGIN
    v_bypass := current_setting('app.bypass_profile_trigger', true);
  EXCEPTION WHEN OTHERS THEN
    v_bypass := 'false';
  END;

  IF v_bypass = 'true' THEN
    RETURN NEW;
  END IF;

  IF (NEW.is_organizer IS DISTINCT FROM OLD.is_organizer)
     OR (NEW.verified IS DISTINCT FROM OLD.verified) THEN
    RAISE EXCEPTION 'Unauthorized: You cannot update privileged profile fields directly.';
  END IF;

  IF (NEW.organizer_type IS DISTINCT FROM OLD.organizer_type) THEN
    IF auth.uid() IS NULL OR auth.uid() <> OLD.id OR COALESCE(OLD.is_organizer, false) <> true THEN
      RAISE EXCEPTION 'Unauthorized: You cannot update privileged profile fields directly.';
    END IF;
  END IF;

  RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION public.update_event_attendees_count()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  IF (TG_OP = 'INSERT') THEN
    UPDATE public.events
    SET attendees = (SELECT count(*) FROM public.tickets WHERE event_id = NEW.event_id)
    WHERE id = NEW.event_id;
    RETURN NEW;
  ELSIF (TG_OP = 'DELETE') THEN
    UPDATE public.events
    SET attendees = (SELECT count(*) FROM public.tickets WHERE event_id = OLD.event_id)
    WHERE id = OLD.event_id;
    RETURN OLD;
  ELSIF (TG_OP = 'UPDATE') THEN
    IF OLD.event_id IS DISTINCT FROM NEW.event_id THEN
      UPDATE public.events
      SET attendees = (SELECT count(*) FROM public.tickets WHERE event_id = OLD.event_id)
      WHERE id = OLD.event_id;

      UPDATE public.events
      SET attendees = (SELECT count(*) FROM public.tickets WHERE event_id = NEW.event_id)
      WHERE id = NEW.event_id;
    END IF;
    RETURN NEW;
  END IF;

  RETURN NULL;
END;
$$;

