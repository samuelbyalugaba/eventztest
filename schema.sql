-- PROFILES Policies
create policy "Public profiles are viewable by everyone" on profiles for select using (true);
create policy "Users can insert their own profile" on profiles for insert with check (auth.uid() = id);
create policy "Users can update their own profile" on profiles for update using (auth.uid() = id);

-- EVENTS Policies
create policy "Events are viewable by everyone" on events for select using (true);
create policy "Organizers can insert events" on events for insert with check (auth.uid() = organizer_id);
create policy "Organizers can update their own events" on events for update using (auth.uid() = organizer_id);
create policy "Organizers can delete their own events" on events for delete using (auth.uid() = organizer_id);

-- TICKETS Policies
create policy "Users can view their own tickets" on tickets for select using (auth.uid() = user_id);
create policy "Organizers can view tickets for their events" on tickets for select using (
  exists (select 1 from events where events.id = tickets.event_id and events.organizer_id = auth.uid())
);
create policy "Users can insert tickets (purchase)" on tickets for insert with check (auth.uid() = user_id);

-- SAVED EVENTS Policies
create policy "Users can view their own saved events" on saved_events for select using (auth.uid() = user_id);
create policy "Users can manage their own saved events" on saved_events for all using (auth.uid() = user_id);

-- FOLLOWS Policies
create policy "Follows are viewable by everyone" on follows for select using (true);
create policy "Users can manage their own follows" on follows for all using (auth.uid() = follower_id);

-- POSTS Policies
create policy "Posts are viewable by everyone" on posts for select using (true);
create policy "Users can insert posts" on posts for insert with check (auth.uid() = user_id);
create policy "Users can update their own posts" on posts for update using (auth.uid() = user_id);
create policy "Users can delete their own posts" on posts for delete using (auth.uid() = user_id);

-- POST LIKES/COMMENTS/SAVES Policies
create policy "Public interactions viewable by everyone" on post_likes for select using (true);
create policy "Users manage own likes" on post_likes for all using (auth.uid() = user_id);

create policy "Public comments viewable by everyone" on post_comments for select using (true);
create policy "Users manage own comments" on post_comments for all using (auth.uid() = user_id);

create policy "Users manage own saved posts" on saved_posts for all using (auth.uid() = user_id);

-- CONVERSATIONS/MESSAGES Policies
create policy "Users can view their conversations" on conversations for select using (
  auth.uid() = participant1_id or auth.uid() = participant2_id
);
create policy "Users can insert conversations" on conversations for insert with check (
  auth.uid() = participant1_id or auth.uid() = participant2_id
);

create policy "Users can view messages in their conversations" on messages for select using (
  exists (
    select 1 from conversations 
    where conversations.id = messages.conversation_id 
    and (conversations.participant1_id = auth.uid() or conversations.participant2_id = auth.uid())
  )
);
create policy "Users can insert messages" on messages for insert with check (auth.uid() = sender_id);
create policy "Users can update messages (mark read)" on messages for update using (
  exists (
    select 1 from conversations 
    where conversations.id = messages.conversation_id 
    and (conversations.participant1_id = auth.uid() or conversations.participant2_id = auth.uid())
  )
);

-- STREAM CHAT Policies
create policy "Stream chat is viewable by everyone" on stream_chat_messages for select using (true);
create policy "Users can insert stream chat messages" on stream_chat_messages for insert with check (auth.uid() = user_id);

-- USER MEDIA Policies
create policy "User media is viewable by everyone" on user_media for select using (true);
create policy "Users can manage their own media" on user_media for all using (auth.uid() = user_id);

-- TRIGGERS for updated_at
create or replace function update_updated_at_column()
returns trigger as $$
begin
    new.updated_at = now();
    return new;
end;
$$ language plpgsql;

create trigger update_profiles_updated_at before update on profiles for each row execute procedure update_updated_at_column();
create trigger update_events_updated_at before update on events for each row execute procedure update_updated_at_column();
create trigger update_conversations_updated_at before update on conversations for each row execute procedure update_updated_at_column();