-- Enable UUID extension
create extension if not exists "uuid-ossp";

-- PROFILES (Public profiles for users)
create table public.profiles (
  id uuid references auth.users not null primary key,
  username text unique,
  full_name text,
  avatar_url text,
  bio text,
  is_organizer boolean default false,
  verified boolean default false,
  location text,
  cover_url text,
  organizer_type text, -- e.g., 'Club', 'Festival', 'Individual'
  phone text,
  website text,
  social_links jsonb default '{}'::jsonb, -- { instagram: '', facebook: '', twitter: '' }
  contact_email text,
  notification_settings jsonb default '{"emailNotifications": true, "pushNotifications": true, "eventReminders": true, "ticketSales": true, "newFollowers": true, "streamAlerts": true, "weeklyReport": true, "marketingEmails": false}'::jsonb,
  streaming_settings jsonb default '{"defaultQuality": "1080p", "autoRecord": true, "chatEnabled": true, "reactionsEnabled": true, "multiCamera": false, "lowLatency": true}'::jsonb,
  privacy_settings jsonb default '{"profileVisibility": "public", "showEmail": false, "showPhone": false, "allowMessages": true, "showFollowers": true, "showStats": true}'::jsonb,
  payment_settings jsonb default '{"paymentMethod": "bank"}'::jsonb,
  preferences jsonb default '{}'::jsonb, -- Generic user preferences (e.g. recent searches)
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- EVENTS
create table public.events (
  id bigint generated by default as identity primary key,
  organizer_id uuid references public.profiles(id) not null,
  title text not null,
  description text,
  date date not null,
  time time not null,
  location text not null,
  city text, -- 'dar', 'zanzibar', 'atlanta', 'newyork', etc.
  category text not null,
  subcategory text,
  price_range text, -- Display text like "TSh 20,000 - 50,000"
  image_url text,
  views integer default 0,
  status text default 'published', -- 'published', 'draft', 'cancelled'
  
  -- JSONB columns for structured data
  streaming jsonb default '{}'::jsonb, 
  -- Structure: { available: boolean, quality: string, virtualPrice: string, isLive: boolean, liveViewers: number, replayAvailable: boolean, features: string[], playback_url: string }
  
  ticket_tiers jsonb default '[]'::jsonb,
  -- Structure: [{ name: string, price: string, priceNumeric: number, available: number, features: string[], color: string }]
  
  event_highlights jsonb default '[]'::jsonb,
  -- Structure: [{ image: string, video: string, caption: string, type: string, mediaType: string }]
  
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- TICKETS (Purchased tickets)
create table public.tickets (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) not null,
  event_id bigint references public.events(id) not null,
  ticket_number text not null unique,
  barcode text not null, -- Could be same as ticket_number or separate
  price text not null,
  purchase_date timestamp with time zone default timezone('utc'::text, now()) not null,
  customer_name text not null,
  customer_email text not null,
  ticket_type text not null, -- 'Normal', 'VIP', 'VVIP'
  status text default 'valid', -- 'valid', 'used', 'refunded'
  qr_code text
);

-- SAVED EVENTS
create table public.saved_events (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) not null,
  event_id bigint references public.events(id) not null,
  is_reminder boolean default false,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  unique(user_id, event_id)
);

-- FOLLOWS
create table public.follows (
